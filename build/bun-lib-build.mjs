#!/usr/bin/env bun
/**
 * Library build helper: `bun build` for workspace packages (ADR 0028 / PRD-0020 R18).
 *
 * Replaces tsdown as the official JS build entry for published library packages.
 * Behaviour:
 *   - Bundles each entry with code splitting enabled by default.
 *   - Treats every `dependencies` / `peerDependencies` / `optionalDependencies`
 *     name as external (so node_modules are never inlined), unless listed in
 *     `--always-bundle` / matched by `--bundle-workspace`.
 *   - Workspace siblings (`@byfriends/*`) are external by default; pass
 *     `--bundle-workspace` to inline them (node-sdk, vis-server, cli).
 *   - Raw `.md` / `.yaml` imports are loaded as text via `loader` (same role as
 *     the former tsdown `raw-text-plugin` / root `bunfig.toml` loaders).
 *   - Emits `.mjs` entry/chunk names to match `publishConfig.exports`.
 *
 * Type declarations are generated by a separate step (`bun-lib-dts.mjs` or a
 * package-specific pipeline such as api-extractor), because `bun build` does
 * not emit `.d.ts`.
 *
 * Usage (run from the package directory):
 *   bun ../../build/bun-lib-build.mjs <entry1> [entry2...] [options]
 *
 * Options:
 *   --out <dir>              Output directory (default: dist)
 *   --root <dir>             Root for multi-entry path layout (default: src if present)
 *   --target <node|bun>      Bun.build target (default: node)
 *   --banner <text>          Prepend a banner to every output file
 *   --banner-dirname         Prepend the CJS __dirname/__filename ESM shim
 *   --shebang                Prepend `#!/usr/bin/env node` (combined with other banners)
 *   --define <k=v>           Define replacement (repeatable). Value is used as-is
 *                            (pass JSON.stringify'd strings from the caller).
 *   --always-bundle <name>   Do not externalize this package (repeatable)
 *   --never-bundle <name>    Force-externalize this package (repeatable)
 *   --bundle-workspace       Inline all `@byfriends/*` packages
 *   --no-splitting           Disable code splitting
 *   --no-clean               Do not wipe outdir before build
 *   --external <name>        Extra external (repeatable)
 */
import { readFile, rm } from 'node:fs/promises';
import path from 'node:path';

const DIRNAME_BANNER = [
  "import { fileURLToPath as __cjsShimFileURLToPath } from 'node:url';",
  "import { dirname as __cjsShimDirname } from 'node:path';",
  'const __filename = __cjsShimFileURLToPath(import.meta.url);',
  'const __dirname = __cjsShimDirname(__filename);',
].join('\n');

const SHEBANG = '#!/usr/bin/env node';

const args = process.argv.slice(2);
const entries = [];
let outDir = 'dist';
let root = undefined;
let target = 'node';
let splitting = true;
let clean = true;
let bundleWorkspace = false;
const alwaysBundle = new Set();
const neverBundle = new Set();
const extraExternal = new Set();
const define = {};
const bannerParts = [];

for (let i = 0; i < args.length; i++) {
  const a = args[i];
  if (a === '--out' || a === '--outdir') {
    outDir = args[++i];
  } else if (a.startsWith('--out=')) {
    outDir = a.slice('--out='.length);
  } else if (a.startsWith('--outdir=')) {
    outDir = a.slice('--outdir='.length);
  } else if (a === '--root') {
    root = args[++i];
  } else if (a.startsWith('--root=')) {
    root = a.slice('--root='.length);
  } else if (a === '--target') {
    target = args[++i];
  } else if (a.startsWith('--target=')) {
    target = a.slice('--target='.length);
  } else if (a === '--banner') {
    bannerParts.push(args[++i]);
  } else if (a.startsWith('--banner=')) {
    bannerParts.push(a.slice('--banner='.length));
  } else if (a === '--banner-dirname') {
    bannerParts.push(DIRNAME_BANNER);
  } else if (a === '--shebang') {
    bannerParts.push(SHEBANG);
  } else if (a === '--define') {
    const pair = args[++i];
    const eq = pair.indexOf('=');
    if (eq <= 0) {
      console.error(`bun-lib-build: invalid --define ${pair} (expected KEY=VALUE)`);
      process.exit(1);
    }
    define[pair.slice(0, eq)] = pair.slice(eq + 1);
  } else if (a.startsWith('--define=')) {
    const pair = a.slice('--define='.length);
    const eq = pair.indexOf('=');
    if (eq <= 0) {
      console.error(`bun-lib-build: invalid --define ${pair} (expected KEY=VALUE)`);
      process.exit(1);
    }
    define[pair.slice(0, eq)] = pair.slice(eq + 1);
  } else if (a === '--always-bundle') {
    alwaysBundle.add(args[++i]);
  } else if (a.startsWith('--always-bundle=')) {
    alwaysBundle.add(a.slice('--always-bundle='.length));
  } else if (a === '--never-bundle') {
    neverBundle.add(args[++i]);
  } else if (a.startsWith('--never-bundle=')) {
    neverBundle.add(a.slice('--never-bundle='.length));
  } else if (a === '--external') {
    extraExternal.add(args[++i]);
  } else if (a.startsWith('--external=')) {
    extraExternal.add(a.slice('--external='.length));
  } else if (a === '--bundle-workspace') {
    bundleWorkspace = true;
  } else if (a === '--no-splitting') {
    splitting = false;
  } else if (a === '--no-clean') {
    clean = false;
  } else if (a === '--clean') {
    clean = true;
  } else if (a.startsWith('-')) {
    console.error(`bun-lib-build: unknown option ${a}`);
    process.exit(1);
  } else {
    entries.push(a);
  }
}

if (entries.length === 0) {
  console.error('bun-lib-build: no entry files provided');
  process.exit(1);
}

const cwd = process.cwd();
const manifest = JSON.parse(await readFile(path.join(cwd, 'package.json'), 'utf8'));
const depNames = new Set([
  ...Object.keys(manifest.dependencies ?? {}),
  ...Object.keys(manifest.peerDependencies ?? {}),
  ...Object.keys(manifest.optionalDependencies ?? {}),
]);

// Default workspace externals (overridden by --bundle-workspace / --always-bundle).
const workspacePackages = [
  '@byfriends/agent-core',
  '@byfriends/sdk',
  '@byfriends/kosong',
  '@byfriends/kaos',
  '@byfriends/oauth',
  '@byfriends/vis-server',
  '@byfriends/vis-web',
  '@byfriends/cli',
];

function shouldAlwaysBundle(name) {
  if (alwaysBundle.has(name)) return true;
  if (bundleWorkspace && name.startsWith('@byfriends/')) return true;
  for (const pattern of alwaysBundle) {
    if (pattern.endsWith('/*') && name.startsWith(pattern.slice(0, -1))) return true;
  }
  return false;
}

const externals = new Set();
for (const name of depNames) {
  if (!shouldAlwaysBundle(name)) externals.add(name);
}
if (!bundleWorkspace) {
  for (const name of workspacePackages) {
    if (!shouldAlwaysBundle(name)) externals.add(name);
  }
}
for (const name of neverBundle) externals.add(name);
for (const name of extraExternal) externals.add(name);
// always-bundle wins over accidental never-bundle of the same name only when
// explicitly listed as always-bundle (never-bundle is applied after deps, so
// re-remove always-bundle names).
const alwaysBundleWins = [];
for (const name of externals) {
  if (shouldAlwaysBundle(name) && !neverBundle.has(name)) {
    alwaysBundleWins.push(name);
  }
}
for (const name of alwaysBundleWins) {
  externals.delete(name);
}

// Infer root from first entry under src/ when multi-entry layout matters.
if (root === undefined) {
  const first = entries[0] ?? '';
  if (first.includes('/src/') || first.startsWith('src/') || first.startsWith('./src/')) {
    root = 'src';
  }
}

if (clean) {
  await rm(path.join(cwd, outDir), { recursive: true, force: true });
}

const banner = bannerParts.length > 0 ? bannerParts.join('\n') : undefined;

const buildOptions = {
  entrypoints: entries.map((e) => path.resolve(cwd, e)),
  outdir: path.resolve(cwd, outDir),
  format: 'esm',
  target,
  splitting,
  sourcemap: 'linked',
  external: [...externals],
  naming: {
    entry: '[dir]/[name].mjs',
    chunk: '[name]-[hash].mjs',
  },
  // Critical: default Bun md loader emits HTML; we need raw strings (PRD R18).
  loader: {
    '.md': 'text',
    '.yaml': 'text',
  },
};

if (root !== undefined) {
  buildOptions.root = path.resolve(cwd, root);
}
if (banner !== undefined) {
  buildOptions.banner = banner;
}
if (Object.keys(define).length > 0) {
  buildOptions.define = define;
}

const result = await Bun.build(buildOptions);

if (!result.success) {
  for (const log of result.logs) console.error(log);
  process.exit(1);
}

const outs = result.outputs.filter((o) => o.kind === 'entry-point' || o.kind === 'chunk');
console.log(`bun-lib-build: ${outs.length} file(s) → ${outDir}/ (${externals.size} external)`);
